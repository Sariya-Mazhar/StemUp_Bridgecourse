
public class Prog5 {

        public static double addNumbers(double n1, double n2) {
            return n1 + n2;
        }
    
        public static void main(String[] args) {
            double n1 = 0;
            double n2 = 0.3;
    
            double sum = addNumbers(n1, n2);
    
            System.out.println("The sum is: " + sum);
        }
    }
    